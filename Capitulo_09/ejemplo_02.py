# Definir una función simple
def saludar():
    """Imprime un saludo en la consola"""
    print("¡Hola desde mi primera función!")

# Para ejecutar la función, debes llamarla
saludar()  # Salida: ¡Hola desde mi primera función!

# Puedes llamarla múltiples veces
saludar()
saludar()
saludar()  # Cada vez que la llamas, se ejecuta