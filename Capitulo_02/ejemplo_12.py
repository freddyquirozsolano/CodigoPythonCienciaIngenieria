# Ejemplos de booleanos
esta_activo = True
tiene_seguro = False
es_mayor_de_edad = True
sistema_operativo = False

# Operaciones booleanas
resultado_and = True and False   # False (ambos deben ser True)
resultado_or = True or False     # True (al menos uno debe ser True)
resultado_not = not True         # False (invierte el valor)

# Comparaciones (devuelven booleanos)
es_mayor = 10 > 5        # True
es_igual = 10 == 5       # False
es_diferente = 10 != 5   # True
es_menor_igual = 5 <= 5  # True