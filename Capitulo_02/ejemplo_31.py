# Ley de Hooke: Fuerza en un resorte
constante_k = 500  # N/m
desplazamiento = 0.05  # m
fuerza = constante_k * desplazamiento
print(f"Fuerza del resorte: {fuerza} N")

# Segunda ley de Newton
masa = 50  # kg
aceleracion = 9.8  # m/s²
fuerza = masa * aceleracion
print(f"Fuerza gravitacional: {fuerza} N")

# Cálculo de presión
fuerza = 1000  # N
area = 0.05  # m²
presion_pa = fuerza / area
presion_kpa = presion_pa / 1000
print(f"Presión: {presion_kpa} kPa")