# Operadores de asignaci√≥n compuesta
contador = 10

# Forma larga
contador = contador + 5   # contador ahora es 15

# Forma corta (equivalente)
contador += 5             # contador ahora es 20

# Otros operadores compuestos
contador -= 3   # contador = contador - 3   (ahora es 17)
contador *= 2   # contador = contador * 2   (ahora es 34)
contador //= 5  # contador = contador // 5  (ahora es 6)
contador **= 2  # contador = contador ** 2  (ahora es 36)