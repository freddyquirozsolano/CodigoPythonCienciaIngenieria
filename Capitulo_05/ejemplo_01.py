# Ejemplo simple: Contador
contador = 1
while contador <= 5:
    print(f"Iteración {contador}")
    contador += 1  # Incrementar el contador

print("Bucle terminado")

# Salida:
# Iteración 1
# Iteración 2
# Iteración 3
# Iteración 4
# Iteración 5
# Bucle terminado